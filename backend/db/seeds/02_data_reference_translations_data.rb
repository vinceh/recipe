def deep_symbolize_keys(hash)
  hash.each_with_object({}) do |(key, value), result|
    result[key.to_sym] = case value
    when Hash
      deep_symbolize_keys(value)
    when Array
      value.map { |item| item.is_a?(Hash) ? deep_symbolize_keys(item) : item }
    else
      value
    end
  end
end

def apply_data_reference_translations
  locale_map = {
    'ja' => :'ja',
    'ko' => :'ko',
    'zh_tw' => :'zh-tw',
    'zh_cn' => :'zh-cn',
    'es' => :'es',
    'fr' => :'fr'
  }

  total_created = 0

  DATA_REFERENCE_TRANSLATIONS.each do |reference_type, type_data|
    puts "Processing #{reference_type}..."

    type_data.each do |locale_key, translations|
      next if locale_key == 'en'

      locale = locale_map[locale_key]
      puts "  #{locale}..."

      I18n.with_locale(locale) do
        translations.each do |key, translated_name|
          ref = DataReference.find_by(reference_type: reference_type, key: key)

          if ref
            ref.display_name = translated_name
            ref.save(validate: false)
          end
        end
      end

      translations_count = translations.count
      total_created += translations_count
      puts "    ✓ #{translations_count} translations created"
    end
  end

  puts "✓ Data reference translations applied! (#{total_created} total)"
end
